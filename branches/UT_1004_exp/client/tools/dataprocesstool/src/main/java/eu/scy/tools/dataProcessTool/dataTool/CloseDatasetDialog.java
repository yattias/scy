    private void initGUI(){
        // parametres dans le texte des rb
        String s = owner.getBundleString("LABEL_CLOSE_DATASET");
        s = MyUtilities.replace(s,0, dataset.getName());
        rbClose.setText(s);
        s = owner.getBundleString("LABEL_DELETE_DATASET");
        s = MyUtilities.replace(s,0, dataset.getName());
        rbDelete.setText(s);
        this.rbClose.setSize(60+MyUtilities.lenghtOfString(this.rbClose.getText(), getFontMetrics(this.rbClose.getFont())), this.rbClose.getHeight());
        // rb suppr
        this.rbDelete.setSize(60+MyUtilities.lenghtOfString(this.rbDelete.getText(), getFontMetrics(this.rbDelete.getFont())), this.rbDelete.getHeight());
        // bouton Ok
        this.buttonOk.setSize(MyUtilities.lenghtOfString(this.buttonOk.getText(), getFontMetrics(this.buttonOk.getFont())), this.buttonOk.getHeight());
        // bouton Annuler
        this.buttonCancel.setSize(MyUtilities.lenghtOfString(this.buttonCancel.getText(), getFontMetrics(this.buttonCancel.getFont())), this.buttonCancel.getHeight());
    }


    private void validDialog(){
        boolean close = rbClose.isSelected();
        boolean isOk = true;
        if(close)
            isOk = owner.closeDataset(dataset);
        else
            isOk =owner.removeDataset(dataset);
        if(isOk)
            this.dispose();
    }
    
